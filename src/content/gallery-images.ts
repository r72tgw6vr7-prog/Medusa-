const PREPARED_GALLERY_IMAGES: GalleryFilterImage[] = GALLERY_IMAGES.flatMap((image) => {
  if (DUPLICATE_GALLERY_IMAGE_IDS.has(image.id)) {
    return [];
  }

  const artistId = getArtistIdFromImage(image);
  if (!artistId) {
    return [];
  }

  return [
    {
      ...image,
      artistId,
      artistName: GALLERY_ARTISTS[artistId].label,
      styleKey: getStyleKey(image.style),
    },
  ];
});

export const getHeroImages = (): GalleryFilterImage[] => {
  return PREPARED_GALLERY_IMAGES.filter((img) => img.priority === true);
};

export const getGalleryPreviewImages = (limit = 8): GalleryFilterImage[] => {
  return PREPARED_GALLERY_IMAGES.slice(0, limit);
};

export const getImageById = (id: string): GalleryFilterImage | undefined => {
  return PREPARED_GALLERY_IMAGES.find((img) => img.id === id);
};

export const getLocalizedGalleryImages = (language: GalleryLocale): GalleryFilterImage[] => {
  return PREPARED_GALLERY_IMAGES.map((image) => ({
    ...image,
    ...getLocalizedImageCopy(image, language),
  }));
};

export const getGalleryArtistOptions = (): GalleryArtistOption[] => {
  const visibleArtistIds = new Set(PREPARED_GALLERY_IMAGES.map((image) => image.artistId));
  return Object.values(GALLERY_ARTISTS).filter((artist) => visibleArtistIds.has(artist.id));
};

/**
 * Generate srcset for responsive images
 * @param optimizedSrc Base path without size suffix
 * @param format 'avif' or 'webp'
 * @param sizes Array of widths (e.g., [400, 640, 960])
 */
export const generateSrcSet = (
  optimizedSrc: string,
  format: 'avif' | 'webp',
  sizes: number[] = [400, 640, 960],
): string => {
  const encodedBase = encodeURI(optimizedSrc);
  return sizes.map((size) => `${encodedBase}-${size}w.${format} ${size}w`).join(', ');
};

/**
 * Generate picture element sources for a gallery image
 */
export const getImageSources = (
  image: GalleryImage,
  sizes: string = '(max-width: 767px) 100vw, 50vw',
) => {
  return {
    avifSrcSet: generateSrcSet(image.optimizedSrc, 'avif'),
    webpSrcSet: generateSrcSet(image.optimizedSrc, 'webp'),
    fallbackSrc: image.src,
    sizes,
    alt: image.alt,
    width: image.width,
    height: image.height,
    loading: image.priority ? 'eager' : ('lazy' as const),
  };
};

