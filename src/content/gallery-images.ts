const getStyleKey = (style: string): string => {
  return style
    .toLowerCase()
    .normalize('NFD')
    .replace(/\p{Diacritic}/gu, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
};

const getLocalizedStyleLabel = (style: string, language: GalleryLocale): string => {
  return GALLERY_STYLE_LABELS[style]?.[language] ?? style;
};

const getLocalizedImageCopy = (
  image: GalleryFilterImage,
  language: GalleryLocale,
): Pick<GalleryImage, 'alt' | 'title' | 'category' | 'style'> => {
  if (language === 'de') {
    return {
      alt: image.alt,
      title: image.title,
      category: image.category,
      style: image.style,
    };
  }

  const localizedStyle = getLocalizedStyleLabel(image.style, language);
  const localizedCategory = getLocalizedStyleLabel(image.category, language);

  return {
    alt: `${localizedStyle} tattoo, piece ${image.id} from Medusa Studio`,
    title: `${localizedCategory} Piece ${image.id}`,
    category: localizedCategory,
    style: localizedStyle,
  };
};

const getArtistIdFromImage = (image: GalleryImage): GalleryArtistId | null => {
  const filename = image.src.split('/').pop()?.toLowerCase() ?? '';

  if (filename.startsWith('debi-')) return 'debi';
  if (filename.startsWith('loui-')) return 'loui';
  if (filename.startsWith('luz-')) return 'luz';

  return null;
};

